#include "ft_printf.h"

int	ft_pointer(unsigned long n)
{
	int	len;

	len = 0;
	if (n == 0)
		return (len += (write(1, "(nil)", 5)));
	write(1, "0x", 2);
	len += 2 + ft_itohex(n, 32);
	return (len);
}
